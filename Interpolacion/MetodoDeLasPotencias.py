def metodo_de_potencias(M):
    
    L = M.shape[0]
    
    v = np.ones(L)
    v_vieja = np.array(np.zeros(L))
    
    eps = 1e-15
    
    while linalg.norm(v - v_vieja) > eps:
        v_vieja = v
        v = np.dot(M, v)
        v /= linalg.norm(v)
        
        #print v
        
    return v
